package com.infosys;

public class CalculationUtil {

    public int add(int a, int b)
    {
        return a+b;
    }

    public int mult(int a, int b)
    {
        return a*b;
    }

    public String parseString(String n)
    {
        return n;
    }

    public int div(int a, int b)
    {
        return a/b;
    }

    public boolean isEven(int n)
    {
        return n%2 == 0;
    }
}
